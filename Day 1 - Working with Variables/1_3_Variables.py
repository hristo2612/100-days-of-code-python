# Working with Variables in Python

# Here we will be working with two variables and swap their values
value_one = input("Value One: ")
value_two = input("Value Two: ")

# Swap the values for the variables
temp = value_one
value_one = value_two
value_two = temp

print("Swapped Value One: " + value_one)
print("Swapped Value Two: " + value_two)
